    private void SignInButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SignInButtonMouseClicked
        try{
            String name = logName.getText();
            String username = LogUsername.getText();
            long password = Long.parseLong(logPassword.getText());
            FileInputStream fileIn = new FileInputStream("C:\\Users\\Sango\\Documents\\MASSLINE Tech Inc\\Coding\\Ragistration files\\"+name+".mc");
            ObjectInputStream objectIn = new ObjectInputStream(fileIn);
            Person per = (Person)objectIn.readObject();
            if(per.getBlocked() == 1){// If active
                if((per.getUserName().equals(username))&&(per.getPassWord()==password)){
                    person = per;
                    JOptionPane.showMessageDialog(null, "Loging in!");
                    PaneLogin.setVisible(false);
                    PaneMenu.setVisible(true);
                }
                else if(name.isEmpty() || logPassword.getText().isEmpty()){
                    JOptionPane.showMessageDialog(null, "Please enter all fields!");
                }
                else{
                    JOptionPane.showMessageDialog(null, "\tIncorrect username or password!");
                }
                if(fileIn == null){
                    JOptionPane.showMessageDialog(null, "Not a member. Please register!");
                }
            }
            else{// If blocked
                JOptionPane.showMessageDialog(null, "This contact has been BLOCKED by MassLine, Inc. !");
            }
                
            fileIn.close();
            objectIn.close();
        }catch(IOException i){
            JOptionPane.showMessageDialog(null,i.getMessage());//"You are not registed. Please register!");
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please enter all fields!");
        }
        catch(ClassNotFoundException c){
            JOptionPane.showMessageDialog(null, logName.getText()+" is not a member!");
        }
        
        MainUsername.setText(person.getUserName());
        
            try{
                BufferedImage imageicon = ImageIO.read(new File(person.getImageAddress()));
                System.out.println(person.getImageAddress());
                person.imageSet(imageicon);
                }
            catch(IOException | NullPointerException e){
                System.out.println(e.getMessage());
                }
        
            try{
                BufferedImage imagez =person.getImage();
                Image image = imagez.getScaledInstance(MenuImage.getWidth(), MenuImage.getHeight(), Image.SCALE_SMOOTH);
                MenuImage.setIcon(new ImageIcon(image));
            }
            catch(Exception e){
                //JOptionPane.showMessageDialog(null, "No profile pic available!");
            }
    }//GEN-LAST:event_SignInButtonMouseClicked

